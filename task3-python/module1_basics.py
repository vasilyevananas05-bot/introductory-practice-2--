# задание 6 код. Прибыль и рентабельность. Запросить выручку и затраты, вычислить прибыль и рентабельность (прибыль / выручка × 100%), вывести оба показателя
revenue = float(input("Выручка: "))
costs = float(input("Затраты: "))
profit = revenue - costs
profitability = (profit / revenue) * 100
print(f"Прибыль: {profit}")
print(f"Рентабельность: {profitability}%")

